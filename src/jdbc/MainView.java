    public static void loadData() {
        try {
            model = new DefaultListModel();
            list = new ArrayList<>();
            String sqlQuery = "SELECT * FROM pegawai";
            Connection conn = (Connection) DBController.configDB();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sqlQuery);
            while (res.next()) {
                model.addElement(res.getString(2));
                list.add(new Pegawai(Integer.parseInt(res.getString(1)), res.getString(2), res.getString(3), res.getDate(4)));
            }
            jList1.setModel(model);
        } catch (Exception e) {}
    }
    
    public static Pegawai getPegawai() {
        return list.get(jList1.getSelectedIndex());
    }
    
