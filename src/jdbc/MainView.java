    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int dialog = JOptionPane.showConfirmDialog(this, "Hapus "+jList1.getSelectedValue().toString()+"?", "WARNING", JOptionPane.YES_NO_OPTION);
        if (dialog == JOptionPane.YES_OPTION) {
            try {
                String sqlQuery = "DELETE FROM pegawai WHERE id=" + list.get(jList1.getSelectedIndex()).getId();
                Connection conn = (Connection) DBController.configDB();
                PreparedStatement pst = conn.prepareStatement(sqlQuery);
                pst.execute();
                loadData();
            } catch (Exception e) {}
        }
    }//GEN-LAST:event_jButton3ActionPerformed

